# Write a program that converts a string to uppercase.
# Algorithm: 1_Input a string. 2_Use the upper()  method to convert the string to uppercase. 3_Print the converted string.

#Input a string 
input_str = "hello world"
print("Original string:", input_str)

#Convert the string to uppercase
upper_str = input_str.upper()
print("String after conversion to uppercase:". upper_str)