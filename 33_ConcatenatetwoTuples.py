# Write a program to concatenate two tuples.
# Algorithm: 1_Initialize two tuples with the desired elements. 2_Use the + operator to concatenate the two tuples together. 3_Print the resulting tuple after concatenation.

# Initialize two tuples
tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)

#Concatenate the two tuples together
combined_tuple = tuple1 + tuple2

#Print the resulting tuple
print("Tuple after concatenation:", combined_tuple)