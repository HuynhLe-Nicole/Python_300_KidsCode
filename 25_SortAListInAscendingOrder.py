#Write a program that sorts a list in ascending order. In Python, we can use the sort() method of the list object to achieve this.
#Algorithm:
#1. Initialize an empty list.
#2. Print the original list.
#3. Use the sort() method to sort the list in ascending order.
#4. Print the sorted list.

#Initialize an empty list
my_list = [34, 1, 23, 4, 3, 12, 45, 33, 6, 22]

#Print the original list
print("Original list:", my_list)

#Sort the list in ascending order
my_list.sort()

#Print the sorted list
print("Sorted list in ascending order:", my_list)