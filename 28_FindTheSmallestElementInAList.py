# Write a program to find the smallest element in a list.
# Algotrithm: 1_Initialize the initial list. 2_Use the min() function to find the smallest element in the list. 3_Print the smallest element.

#Initialize the initial list
my_list = [34, 1, 23, 4, 3, 12, 45, 33, 6, 22]

#Find the smallest element  in the list
