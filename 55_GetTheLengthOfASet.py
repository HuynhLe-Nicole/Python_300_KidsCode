# Write a program to obtian the length of a set 
# Algorithm: 1_Initialize an initial set. 2_Get the length of the set using the len() function.

#Initialize an initial set
my_set = {'apple', 'banana', 'cherry'}
print("Initial set:", my_set)

#Get the length of the set
length = len(my_set)
print("Length of the set:", length)